		Descriptif du poste
		A Paris ou en région, nous recherchons les meilleurs professionnels, issus de cursus d’excellence prioritairement spécialisés en droit social, mais également des DJCE, des écoles de commerce et des IEP. Une double formation et/ou un séjour à l’étranger est apprécié. 

		Les missions

		Dans ce cadre, il est l’interlocuteur privilégié des IRP et veille à développer et maintenir un dialogue social de qualité entre les représentants et la Direction de GreenFlex. Il anime au quotidien les relations avec les instances et est en charge de préparer les réunions mensuelles du CSE et CSSCT :

		Fixation de l’ordre du jour en lien avec la Direction et les priorités de l’entreprise.
		Organisation des réunions (convocations, ordre du jour, préparation des documents et supports présentés, réponse aux questions, relecture des PV).
		Assure la mise à jour du document unique d’évaluation des risques (DUERP), propose et met en place des plans d’actions et en assure le suivi.
		Organise et mène des points de suivi hebdomadaires des sujets en-cours avec le CSE.
		Il participe aux projets en cours et aux négociations d’accords d’entreprise et de rédaction des projets d’accord en lien avec le cabinet en droit social qui accompagne GreenFlex.
		Il organise le cas échéant les élections du personnel.
		Il anime le debriefing des CSE auprès de l’équipe RH afin de transmettre les informations essentielles nécessaires à la compréhension des enjeux et de l’actualité.

